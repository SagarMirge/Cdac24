package Assignments;
		  
		public class PatternTrn {
			
		public static void main(String args[])   
		{   
		//i for rows and j for columns      
		int i, j, row=6;   
		//outer loop for rows  
		for(i=0; i<row; i++)   
		{   
		//inner loop for columns  
		for(j=0; j<=i; j++)   
		{   
		System.out.print("* ");   
		}   
		System.out.println();   
		}   
		}   
		

	}


